            <h2>¿QUÉ TIPOS DE COOKIES UTILIZA ESTA PÁGINA WEB?</h2>
        </div>
    </div>
    <div class="row mt-5">
        <div class="col-md-12 d-inline-block mt-0">
            <p>Esta página web utiliza los siguientes tipos de cookies: <br><br>

                <p style="font-weight:bold">Cookies de análisis:</p> Son aquéllas que bien tratadas por nosotros o por terceros, nos permiten cuantificar el número de usuarios y así realizar la medición y análisis estadístico de la utilización que hacen los usuarios del servicio ofertado. Para ello se analiza su navegación en nuestra página web con el fin de mejorar la oferta de productos o servicios que le ofrecemos. <br><br>

                <p style="font-weight:bold">Cookies técnicas:</p>Son aquellas que permiten al usuario la navegación a través del área restringida y la utilización de sus diferentes funciones, como por ejemplo, llevar a cambio el proceso de compra de un artículo.<br><br>

                <p style="font-weight:bold">Cookies de personalización:</p> Son aquellas que permiten al usuario acceder al servicio con algunas características de carácter general predefinidas en función de una serie de criterios en el terminal del usuario como por ejemplo serian el idioma o el tipo de navegador a través del cual se conecta al servicio.<br><br>

                <p style="font-weight:bold">Cookies publicitarias:</p> Son aquéllas que, bien tratadas por esta web o por terceros, permiten gestionar de la forma más eficaz posible la oferta de los espacios publicitarios que hay en la página web, adecuando el contenido del anuncio al contenido del servicio solicitado o al uso que realice de nuestra página web. Para ello podemos analizar sus hábitos de navegación en Internet y podemos mostrarle publicidad relacionada con su perfil de navegación.<br><br>

                <p style="font-weight:bold">Cookies de publicidad comportamental:</p> Son aquellas que permiten la gestión, de la forma más eficaz posible, de los espacios publicitarios que, en su caso, el editor haya incluido en una página web, aplicación o plataforma desde la que presta el servicio solicitado. Este tipo de cookies almacenan información del comportamiento de los visitantes obtenida a través de la observación continuada de sus hábitos de navegación, lo que permite desarrollar un perfil específico para mostrar avisos publicitarios en función del mismo.
            </p>

