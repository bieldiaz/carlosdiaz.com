            <h2>Qué es una cookie?</h2>
        </div>
    </div>
    <div class="row mt-5">
        <div class="col-md-12 d-inline-block mt-0">
            <p>Una cookie es un paquete de datos que un navegador web almacena de forma automática en el ordenador de un usuario cuando este visita una página web. La cookie es enviada desde el servidor al visitante de la página web. Posteriormente, cada vez que el usuario visite esa misma página web o alguna otra del mismo dominio, la cookie será leída por el navegador web, sin ser modificada, y devuelta al servidor web.
                Por tanto, una cookie son sólo datos que se almacenan en el ordenador del usuario. Las cookies no son software, tampoco son fragmentos de código, son simplemente datos. Por tanto, en principio las cookies no pueden transmitir y ejecutar virus, ni instalar malware como troyanos o programas de espionaje.</p>

        </div>
